#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		i;

	i = (ft_strlen((char *)src) + 1);
	dest = NULL;
	if (src != NULL)
	{
		if (!(dest = (char*)malloc(sizeof(*dest) * i)))
			return (NULL);
		i = 0;
		while (src[i] != '\0')
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (dest);
}
